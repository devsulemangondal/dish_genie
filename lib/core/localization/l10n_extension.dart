/// Extension to provide compatibility with the old context.t() syntax
/// Maps old dot-notation keys (e.g., 'common.home') to new generated getters
extension L10nExtension on BuildContext {
  static final AppLocalizationsEn _fallbackEn = AppLocalizationsEn();

  static String _applyParams(String text, Map<String, String>? params) {
    if (params == null || params.isEmpty) return text;
    var out = text;
    params.forEach((paramKey, paramValue) {
      out = out.replaceAll('{$paramKey}', paramValue);
    });
    return out;
  }

  /// Get the AppLocalizations instance
  AppLocalizations get l10n => AppLocalizations.of(this)!;

  /// Translate using old dot-notation keys (e.g., 'common.home')
  /// This maps to the new generated getters
  String t(String key, [Map<String, String>? params]) {
    final localizations = AppLocalizations.of(this);
    if (localizations == null) {
      // Debug: Log when localizations is null
      debugPrint(
        '⚠️ [L10n] AppLocalizations.of(context) returned null for key: $key',
      );
      return key;
    }

    // Check for parameterized getters that need special handling
    final parameterizedGetter = _parameterizedMap[key];
    if (parameterizedGetter != null) {
      // Use parameterized getter even if params is null (use empty params)
      final translated = parameterizedGetter(localizations, params);
      if (translated.trim().isNotEmpty) return translated;

      final fallback = parameterizedGetter(_fallbackEn, params);
      return fallback.trim().isNotEmpty ? fallback : key;
    }

    // Map old dot-notation keys to new getter functions
    final getter = _localizationMap[key];
    if (getter == null) {
      // Debug: Log missing keys - these need to be added to the localization map
      debugPrint(
        '⚠️ [L10n] Key not found in localization map: "$key". Available keys: ${_localizationMap.keys.where((k) => k.startsWith(key.split('.').first)).take(5).join(", ")}...',
      );
      // Return key as-is if not found (will need to be added to ARB files and regenerate)
      return key;
    }

    try {
      // Get the translated string
      String translated = getter(localizations);

      // Replace parameters if provided
      translated = _applyParams(translated, params);

      // If translation exists but is empty in the current locale, fall back to English.
      if (translated.trim().isEmpty) {
        translated = _applyParams(getter(_fallbackEn), params);
      }

      return translated.trim().isNotEmpty ? translated : key;
    } catch (e) {
      debugPrint('❌ [L10n] Error getting translation for key "$key": $e');
      return key;
    }
  }

